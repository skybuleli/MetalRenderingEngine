#!/usr/bin/env python3
"""从 metal-shaderconverter 的 *.reflect.json 生成运行时 *.bindings.json 和 C# BindingLayout 子类。"""

from __future__ import annotations

import json
import re
import sys
from pathlib import Path


RESOURCE_CSHARP_TYPE = {
    "SRV": "MscResourceType.Srv",
    "UAV": "MscResourceType.Uav",
    "CBV": "MscResourceType.Cbv",
    "Sampler": "MscResourceType.Sampler",
}


def _pascal_case(name: str) -> str:
    if not name:
        return ""
    parts = re.split(r"[_\-]+", name)
    return "".join(part[:1].upper() + part[1:] for part in parts if part)


def _reflect_stage(reflect_path: Path) -> tuple[str | None, str | None]:
    stem = reflect_path.name.removesuffix(".reflect.json")
    if stem.endswith(".generated"):
        stem = stem.removesuffix(".generated")

    if stem.endswith(".vert"):
        return stem.removesuffix(".vert"), "Vertex"
    if stem.endswith(".frag"):
        return stem.removesuffix(".frag"), "Fragment"
    return None, None


def _generate_binding_cs(shader_base: str, vert_resources: list[dict], frag_resources: list[dict]) -> str:
    class_name = _pascal_case(shader_base) + "BindingLayout"
    has_vert = len(vert_resources) > 0

    lines: list[str] = []
    lines.append("// <auto-generated />")
    lines.append(f"// Generated by generate_bindings.py from {shader_base}.reflect.json")
    lines.append("using MetalRenderingEngine.Binding;")
    lines.append("using MetalRenderingEngine.Metal;")
    lines.append("using MetalRenderingEngine.Shader.Reflection;")
    lines.append("")
    lines.append("namespace MetalRenderingEngine.Demo;")
    lines.append("")
    lines.append(f"internal sealed class {class_name} : ShaderBindingLayout")
    lines.append("{")
    lines.append(f"    public {class_name}()")
    if has_vert:
        lines.append(f'        : base("{shader_base}.vert", "{shader_base}.frag")')
    else:
        lines.append(f'        : base(null, "{shader_base}.frag", hasVertexStage: false)')
    lines.append("    {")
    lines.append("    }")
    lines.append("")

    for entry in vert_resources:
        resource_type = entry["Type"]
        slot = entry["Slot"]
        cs_type = RESOURCE_CSHARP_TYPE.get(resource_type, "MscResourceType.Unknown")
        suffix = f"{resource_type.title()}{slot}"
        method_name = f"SetVertex{suffix}"
        if resource_type in ("SRV", "UAV", "CBV"):
            lines.append(f"    public void {method_name}(MetalBuffer value)")
            lines.append("    {")
            lines.append(f"        DeclareVertexBuffer({slot}, value, {cs_type});")
            lines.append("    }")
        else:
            lines.append(f"    // TODO: {method_name} 暂不支持 vertex stage 的 {resource_type}")
        lines.append("")

    for entry in frag_resources:
        resource_type = entry["Type"]
        slot = entry["Slot"]
        cs_type = RESOURCE_CSHARP_TYPE.get(resource_type, "MscResourceType.Unknown")
        suffix = f"{resource_type.title()}{slot}"
        method_name = f"SetFragment{suffix}"

        if resource_type == "SRV":
            lines.append(f"    public void {method_name}(MetalBuffer value)")
            lines.append("    {")
            lines.append(f"        DeclareFragmentBuffer({slot}, value, {cs_type});")
            lines.append("    }")
            lines.append("")
            lines.append(f"    public void {method_name}(MetalTexture value)")
            lines.append("    {")
            lines.append(f"        DeclareFragmentTexture({slot}, value);")
            lines.append("    }")
        elif resource_type == "Sampler":
            lines.append(f"    public void {method_name}(MetalSamplerState value)")
            lines.append("    {")
            lines.append(f"        DeclareFragmentSampler({slot}, value);")
            lines.append("    }")
        elif resource_type in ("UAV", "CBV"):
            lines.append(f"    public void {method_name}(MetalBuffer value)")
            lines.append("    {")
            lines.append(f"        DeclareFragmentBuffer({slot}, value, {cs_type});")
            lines.append("    }")
        else:
            lines.append(f"    // TODO: {method_name} 暂不支持 fragment stage 的 {resource_type}")
        lines.append("")

    lines.append("}")
    lines.append("")
    return "\n".join(lines)


def generate_one(reflect_path: Path) -> Path:
    with reflect_path.open("r", encoding="utf-8") as f:
        reflection = json.load(f)

    shader_name = reflect_path.name.removesuffix(".reflect.json")
    resources = []
    for entry in reflection.get("TopLevelArgumentBuffer", []):
        resources.append(
            {
                "name": entry.get("Name", ""),
                "type": entry.get("Type", ""),
                "slot": entry.get("Slot", 0),
                "space": entry.get("Space", 0),
                "offset": entry.get("EltOffset", 0),
                "size": entry.get("Size", 0),
            }
        )

    metadata = {
        "version": 1,
        "shader": shader_name,
        "stage": reflection.get("ShaderType", ""),
        "argumentBufferBindPoint": 2,
        "resources": resources,
    }

    out_path = reflect_path.with_name(f"{shader_name}.bindings.json")
    with out_path.open("w", encoding="utf-8") as f:
        json.dump(metadata, f, ensure_ascii=False, indent=2)
        f.write("\n")
    return out_path


def generate_binding_cs(shader_dir: Path) -> int:
    pairs: dict[str, dict[str, list[dict]]] = {}
    skipped = 0

    for reflect_path in sorted(shader_dir.glob("*.reflect.json")):
        base, stage = _reflect_stage(reflect_path)
        if base is None or stage is None:
            skipped += 1
            continue

        with reflect_path.open("r", encoding="utf-8") as f:
            reflection = json.load(f)

        pairs.setdefault(base, {})[stage] = reflection.get("TopLevelArgumentBuffer", [])

    count = 0
    for base, stages in pairs.items():
        vert = stages.get("Vertex", [])
        frag = stages.get("Fragment", [])
        if not vert and not frag:
            continue

        cs_code = _generate_binding_cs(base, vert, frag)
        out_path = shader_dir / f"{base}.BindingLayout.g.cs"
        with out_path.open("w", encoding="utf-8") as f:
            f.write(cs_code)
        print(f"[bindinggen:cs] generated {out_path}")
        count += 1

    if skipped:
        print(f"[bindinggen:cs] skipped {skipped} non-graphics reflection file(s)")

    return count


def main(argv: list[str]) -> int:
    emit_cs = "--emit-cs" in argv
    args = [a for a in argv if a != "--emit-cs"]

    if len(args) != 2:
        print("usage: generate_bindings.py [--emit-cs] <shader-output-dir>", file=sys.stderr)
        return 2

    shader_dir = Path(args[1])
    if not shader_dir.exists():
        print(f"[bindinggen] skip: {shader_dir} does not exist")
        return 0

    count = 0
    for reflect_path in sorted(shader_dir.glob("*.reflect.json")):
        out_path = generate_one(reflect_path)
        print(f"[bindinggen] {out_path}")
        count += 1
    print(f"[bindinggen] Done. {count} binding metadata file(s) generated.")

    if emit_cs:
        cs_count = generate_binding_cs(shader_dir)
        print(f"[bindinggen:cs] Done. {cs_count} BindingLayout class(es) generated.")

    return 0


if __name__ == "__main__":
    raise SystemExit(main(sys.argv))
